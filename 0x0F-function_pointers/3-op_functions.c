#include "3-calc.h"
/**
 * op_add - Adds two numbers and returns sum
 * @a: First number
 * @b: Second  number
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two numbers and returns result
 * @a: First number
 * @b: Second  number
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies two numbers and returns the product
 * @a: First number
 * @b: Second number
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides two numbers and returns divident
 * @a: First number
 * @b: Second  number
 * Return: Divident
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Divides two numbers and returns remainder
 * @a: First number
 * @b: Second number
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
